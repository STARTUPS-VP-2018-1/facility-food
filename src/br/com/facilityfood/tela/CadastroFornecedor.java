    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Fornecedor novoFornecedor = new Fornecedor();        
        
        novoFornecedor.setNomeBarraca(txtNomeBarraca.getText());
        novoFornecedor.setNome(txtNomeFornecedor.getText());
        novoFornecedor.setCnpj(txtCnpjFornecedor.getText());
        novoFornecedor.setNomeUsuario(txtUsuarioFornecedor.getText());
        novoFornecedor.setSenha(txtSenhaFornecedor.getText());
        
        FornecedorInterface fornecedorBusiness = new FornecedorBusiness();
        
        fornecedorBusiness.salvarFornecedor(novoFornecedor);
    }//GEN-LAST:event_jButton1ActionPerformed

